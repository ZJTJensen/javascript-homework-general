var days = 60;

for (var i=days; i > 0; i--){
    if (i>30){
        console.log(i+ " days until my birthday. Such a long time :(");
    }
    if (i<=30 || i!=1){
        console.log(i+" DAYS UNTIL MY BIRTHDAY!!!");
    }
    if (i===1){
        console.log("1 Day until my birthday... no biggies");
    }
}
console.log("ITS MY FREAKING BIRTHDAY!!!!!!!");